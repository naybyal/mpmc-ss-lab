ASSUME CS:CODE, DS:DATA

DATA SEGMENT
	STR1 DB ' SOME STRING$'
	STR2 DB ' ANOTHER STRING$'
	SAME DB ' BOTH STRINGS ARE THE SAME$'
	NOTSAME DB ' THE STRINGS ARE DIFFERENT$' 
DATA ENDS

PRINT MACRO MSG
	LEA DX, MSG
	MOV AH, 09H
	INT 21H
ENDM

CODE SEGMENT
START:
	MOV AX, DATA
	MOV DS, AX
	
	MOV SI, 0
REPEAT:
	MOV AL, [STR1+SI]
	MOV BL, [STR2+SI]
	CMP AL, BL
	JNZ CHANGE
	INC SI
	CMP AL, '$'
	CMP BL, '$'
	JNZ TRUE
	
TRUE:
	PRINT SAME
CHANGE:
	PRINT NOTSAME
STOP:
	MOV AH, 4CH
	INT 21H
CODE ENDS
END START
