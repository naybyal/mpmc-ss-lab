;	LARGEST NUMBER IN A LIST

ASSUME DS:DATA, CS:CODE
DATA SEGMENT
	LIST DB 1,2,4,5,3
	MSG DB 0AH, 0DH, "THE LARGEST ELEMENT IS $"
DATA ENDS

PRINT MACRO MSG
	LEA DX, MSG
	MOV AH, 09H
	INT 21H
ENDM

CODE SEGMENT
START:	MOV AX, DATA
	MOV DS, AX

	MOV SI, 0
	MOV AL, [LIST+SI]
	MOV CL, AL
	INC SI
SEARCH:	MOV AL, [LIST+SI]
	CMP AL, CL
	JG LARGE

COUNT:	INC SI
	CMP SI, 5
	JL SEARCH
	JGE DOWN
LARGE:	MOV CL, AL
	JMP COUNT
DOWN:	MOV AL, CL
	ADD AL, 30H
	PRINT MSG
	MOV DL, AL
	MOV AH, 02H
	INT 21H
	MOV AH, 4CH
	INT 21H	
CODE ENDS
END START
