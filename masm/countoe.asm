ASSUME CS:CODE, DS:DATA

DATA SEGMENT
	LIST DB 1,9,3,5,6,7,2
	LEN EQU 7
	MSG1 DB 0AH, 0DH, 'ODD  $'
	MSG2 DB 0AH, 0DH, 'EVEN  $'
DATA ENDS

PRINT MACRO MSG
	LEA DX, MSG
	MOV AH, 09H
	INT 21H
ENDM

CODE SEGMENT
START:	
	MOV AX, DATA
	MOV DS, AX
	
	MOV CL, LEN
	LEA SI, LIST
	
	MOV BL, 0
	MOV BH, 0
LP1:	
	CMP CL, 0
	JZ STOP
	MOV AL, [SI]
	ROR AL, 1
	JC ODD
	INC BL
	DEC CL
	JMP LP1
ODD:
	INC BH
	INC SI
	DEC CL
	JMP LP1
	
STOP:	
	DEC BL
	INC BH
	PRINT MSG1
	MOV AL, BH
	AAA
	ADD AL, 30H
	MOV DL, AL
	MOV AH, 02H
	INT 21H
	
	PRINT MSG2
	MOV AL, BL
	AAA
	ADD AL, 30H
	MOV DL, AL
	MOV AH, 02H
	INT 21H

	MOV AH, 4CH
	INT 21H
CODE ENDS
END START
