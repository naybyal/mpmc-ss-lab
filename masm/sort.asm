ASSUME CS:CODE, DS:DATA

DATA SEGMENT 
	LIST DB 1,9,5,3,2
DATA ENDS

PRINT MACRO VAR
	MOV CL, 05H
	MOV SI, 01H
UP:	MOV AL, [VAR+SI]
	ADD AL, 30H
	MOV DL, AL
	MOV AH, 02H
	INT 21H
	CMP CL, SI
	INC SI
	DEC CL
	JLT UP
ENDM

CODE SEGMENT
START:	MOV AX, DATA
	MOV DS, AX
	MOV CH, 05H
UP2:	MOV CL, 05H
	LEA SI, LIST

UP1:	MOV AL, [SI]
	MOV BL, [SI+1]
	CMP AL, BL
	JC DOWN
	MOV DL, [SI+1]
	XCHG [SI], DL
	MOV [SI+1], DL

DOWN:	INC SI
	DEC CL
	JNZ UP1
	DEC CH
	JNZ UP2

	PRINT LIST

	MOV AH, 4CH
	INT 21H
CODE ENDS
END START
