ASSUME CS:CODE, DS:DATA
DATA SEGMENT
	STR1 DB 'REVERSE$'
	LEN EQU 7
	STR2 DB 20 DUP('$')
DATA ENDS

PRINT MACRO MSG
	LEA DX, MSG
	MOV AH, 09H
	INT 21H
ENDM

CODE SEGMENT
START:	MOV AX, DATA
	MOV DS, AX
	LEA SI, STR1
	LEA DI, STR2
	ADD SI, LEN-2
	MOV CL, LEN-1
AGAIN:	MOV AL, [SI]
	MOV [DI], AL
	DEC SI
	INC DI
	LOOP AGAIN
	PRINT STR2
	MOV AH, 4CH
	INT 21H
CODE ENDS
END START
